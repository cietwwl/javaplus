package org.hhhhhh.fqzs.core;

public class ButtonLocation {
	int x;
	int y;
	private String id;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public void setId(String id) {
		this.id = id;

	}

	public String getId() {
		return id;
	}
}
